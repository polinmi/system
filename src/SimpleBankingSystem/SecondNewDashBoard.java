    private void dDepositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dDepositButtonActionPerformed
        // Create a deposit function here
        double sum = 0;

        String dep = dDeposit.getText();
        String c = cbBal.getText();
        String Bn = getBND.getText();

        if (Bn.equals("0")) {

            JOptionPane.showMessageDialog(this, "Bank No. Not Exist!");
        } else {

            if (dep.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter money");
            } else {
                int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure to Deposit this Money?", null, JOptionPane.YES_NO_OPTION);
                if (confirmation == JOptionPane.YES_OPTION) {
                    double d = Double.parseDouble(dep);
                    double val = Double.parseDouble(c);

                    sum = d + val;
                    cbBal.setText(String.valueOf(sum));
                    JOptionPane.showMessageDialog(this, "Money Deposited Successfully");
                    dDeposit.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Deposit Cancelled!");
                }

            }
        }


    }//GEN-LAST:event_dDepositButtonActionPerformed

    private void dDepositKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dDepositKeyPressed
        // disableletters and sysmbols
        char c = evt.getKeyChar();
        if (Character.isLetter(c)) {
            dDeposit.setEditable(false);
        } else {
            dDeposit.setEditable(true);
        }
        dDeposit.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
                char c = e.getKeyChar();
                if (!Character.isDigit(c)) {
                    e.consume();
                }
            }
        });
    }//GEN-LAST:event_dDepositKeyPressed

