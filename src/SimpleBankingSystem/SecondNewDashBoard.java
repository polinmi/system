    private void jSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSignUpActionPerformed

        try {
            String bn = Increment.getText();
            String Name = CName.getText();
            String Street = CStreet.getText();
            String City = CCity.getText();
            String Email = CEmail.getText();
            String PhoneN = CPhoneN.getText();

            if (Name.isEmpty() || Street.isEmpty() || City.isEmpty() || Email.isEmpty() || PhoneN.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill out the data");
                return;
            }
            if (PhoneN.length() != 11 || !PhoneN.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "Phone number must be 11 digits");
            } else {

                JOptionPane.showMessageDialog(this, "Account successfully registered!");
                int currentValue = Integer.parseInt(Increment.getText());

                getBN.setText(Increment.getText());
                getBND.setText(Increment.getText());
                getBNW.setText(Increment.getText());
                cFName.setText(CName.getText());
                dCName.setText(CName.getText());
                WdpFName.setText(CName.getText());
                
                Infos.iBn = Increment.getText();
                Infos.iName = CName.getText();
                Infos.iStreet = CStreet.getText();
                Infos.iCity = CCity.getText();
                Infos.iEmail = CEmail.getText();
                Infos.iPhoneN = CPhoneN.getText();
               
                CName.setText("");
                CStreet.setText("");
                CCity.setText("");
                CEmail.setText("");
                CPhoneN.setText("");
                Increment.setText(String.valueOf(currentValue + 1));

            }
            
        } catch (Exception e) {

        }
    }//GEN-LAST:event_jSignUpActionPerformed

