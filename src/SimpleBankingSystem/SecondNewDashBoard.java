    private void wWithdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wWithdrawButtonActionPerformed
        // Withdraw
        double total = 0;

        String bnw = getBNW.getText();
        String c = cbBal.getText();
        String w = wWithdraw.getText();

        try {
            if (bnw.equals("0")) {
                JOptionPane.showMessageDialog(this, "Bank No. not Exist!");
            } else {
                double val = Double.parseDouble(c);
                double value = Double.parseDouble(w);

                if (val < value) {
                    JOptionPane.showMessageDialog(this, "Insufficient balance");
                } else {

                    int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure to Withdraw this Money?", null, JOptionPane.YES_NO_OPTION);
                    if (confirmation == JOptionPane.YES_OPTION) {
                        total = val - value;
                        cbBal.setText(Double.toString(total));
                        JOptionPane.showMessageDialog(this, "Money Withdrew Successfully");
                        wWithdraw.setText("");
                    } else {
                        JOptionPane.showMessageDialog(this, "Withdraw Cancelled!");
                    }
                }
            }
        } catch (Exception e) {
            System.out.println("");
        }


    }//GEN-LAST:event_wWithdrawButtonActionPerformed

