    private void reqUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reqUpdateActionPerformed
        // TODO add your handling code here:

        String Name = AccName.getText();
        String Street = AccStreet.getText();
        String City = AccCity.getText();
        String Email = AccEmail.getText();
        String PhoneN = AccPhoneN.getText();

        if (Name.isEmpty() || Street.isEmpty() || City.isEmpty() || Email.isEmpty() || PhoneN.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill out the data");
            return;
        }
        if (PhoneN.length() != 11 || !PhoneN.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "The phone number must be 11 digits");
            return;
        } else {

            int conf = JOptionPane.showConfirmDialog(this, "Are you sure you want to update your account details?", "Update Request", JOptionPane.YES_NO_OPTION);

            if (conf == JOptionPane.YES_OPTION) {

                RequestUpdate.reqBankno = AccBankNo.getText();
                RequestUpdate.reqName = AccName.getText();
                RequestUpdate.reqStreet = AccStreet.getText();
                RequestUpdate.reqCity = AccCity.getText();
                RequestUpdate.reqEmail = AccEmail.getText();
                RequestUpdate.reqPhoneN = AccPhoneN.getText();

                JOptionPane.showMessageDialog(this, "Update request successful!");

            }
        }

    }//GEN-LAST:event_reqUpdateActionPerformed

