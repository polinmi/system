    private void dDepositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dDepositButtonActionPerformed
        // Deposit button
        double sum = 0;
        String newBalance = dDeposit.getText();
        String BankNo = dBankNo.getText();
        String c = checkBal.getText();

        File f = new File(customerAccountsFilePath);
        File f2 = new File(tempFile);

        if (newBalance.isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please input the field");
        } else {

            int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to deposit this money?", null, JOptionPane.YES_NO_OPTION);
            if (confirmation == JOptionPane.YES_OPTION) {
                double d = Double.parseDouble(newBalance);
                double val = Double.parseDouble(c);

                sum = d + val;
                checkBal.setText(String.valueOf(sum));
                JOptionPane.showMessageDialog(this, "Money Deposited Successfully");

                File f3 = new File(historyData);

                try {
                    FileWriter wrt = new FileWriter(f3, true);

                    wrt.write(BankNo + ", " + "Deposit" + ", " + "Php" + newBalance);
                    wrt.write(System.getProperty("line.separator"));
                    wrt.close();

                } catch (IOException ex) {
                    Logger.getLogger(MightFinal.class.getName()).log(Level.SEVERE, null, ex);
                }

                try (BufferedReader br = new BufferedReader(new FileReader(f2));
                        FileWriter fw = new FileWriter(f)) {

                    String line;
                    boolean found = false;

                    while ((line = br.readLine()) != null) {
                        if (line.startsWith("BankNo: " + BankNo)) {
                            found = true;
                            fw.write(line + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));
                            fw.write("Balance: " + sum + System.getProperty("line.separator"));
                            fw.write(br.readLine() + System.getProperty("line.separator"));

                        } else {
                            fw.write(line + System.getProperty("line.separator"));
                        }
                    }

                    if (!found) {
                        JOptionPane.showMessageDialog(this, "Bank number not found");
                        return;
                    }

                    f.delete();
                    f2.renameTo(f);

                    checkBal.setText(String.valueOf(sum));
                    DdpBal.setText(checkBal.getText());
                    WdpBal.setText(checkBal.getText());
                    dDeposit.setText("");

                } catch (IOException ex) {
                    Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }


    }//GEN-LAST:event_dDepositButtonActionPerformed

