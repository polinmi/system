    private void wWithdrawButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_wWithdrawButtonActionPerformed
        // Withdraw button
        double total = 0;

        String c = checkBal.getText();
        String w = wWithdraw.getText();
        String b = wBankNo.getText();

        if (w.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please input the field");
        } else {
            double val = Double.parseDouble(c);
            double value = Double.parseDouble(w);

            if (val < value) {
                JOptionPane.showMessageDialog(this, "Insufficient balance", "", JOptionPane.INFORMATION_MESSAGE);
            } else {
                int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to withdraw this money?", null, JOptionPane.YES_NO_OPTION);
                if (confirmation == JOptionPane.YES_OPTION) {
                    total = val - value;
                    checkBal.setText(Double.toString(total));
                    JOptionPane.showMessageDialog(this, "Money Withdrawn Successfully");

                    File f3 = new File(historyData);

                    try {
                        FileWriter wrt = new FileWriter(f3, true);

                        wrt.write(b + ", " + "Withdraw" + ", " + "Php" + w);
                        wrt.write(System.getProperty("line.separator"));
                        wrt.close();

                    } catch (IOException ex) {
                        Logger.getLogger(MightFinal.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    File f = new File(customerAccountsFilePath);
                    File f2 = new File(tempFile);

                    try (BufferedReader br = new BufferedReader(new FileReader(f2));
                            FileWriter fw = new FileWriter(f)) {

                        String line;
                        boolean found = false;

                        while ((line = br.readLine()) != null) {
                            if (line.startsWith("BankNo: " + b)) {
                                found = true;
                                fw.write(line + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));
                                fw.write("Balance: " + total + System.getProperty("line.separator"));
                                fw.write(br.readLine() + System.getProperty("line.separator"));

                            } else {
                                fw.write(line + System.getProperty("line.separator"));
                            }
                        }

                        if (!found) {
                            JOptionPane.showMessageDialog(this, "Bank number not found");
                            return;
                        }

                        f.delete();
                        f2.renameTo(f);

                        checkBal.setText(String.valueOf(total));
                        dDeposit.setText("");

                    } catch (IOException ex) {
                        Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    wWithdraw.setText("");
                    WdpBal.setText(checkBal.getText());
                    DdpBal.setText(checkBal.getText());

                } else {
                    JOptionPane.showMessageDialog(this, "Withdrawal Cancelled!");
                }
            }
        }


    }//GEN-LAST:event_wWithdrawButtonActionPerformed

