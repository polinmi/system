    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // Update Button

        String Bnumber = UpbankNo.getText();
        String newName = Upname.getText();
        String newStreet = Upstreet.getText();
        String newCity = Upcity.getText();
        String newEmail = Upemail.getText();
        String newPhone = Upphone.getText();

        File f = new File(userAcc);
        File temp = new File(tempFile);
        //Updating the data
        try (BufferedReader br = new BufferedReader(new FileReader(temp));
                FileWriter fw = new FileWriter(f)) {

            String line;
            boolean found = false;
            boolean deleteNext = false;
            boolean isCurrentAccount = false;
            while ((line = br.readLine()) != null) {

                if (deleteNext) {
                    deleteNext = false;
                    continue;
                }
                String[] parts = line.split(": ");
                if (parts[0].equals("BankNo") && parts[1].equals(Bnumber)) {
                    isCurrentAccount = true;
                } else if (parts[0].equals("BankNo") && !parts[1].equals(Bnumber)) {
                    isCurrentAccount = false;
                }

                if (isCurrentAccount) {
                    if (parts[0].equals("BankNo")) {
                        fw.write("BankNo: " + Bnumber + System.getProperty("line.separator"));
                    } else if (parts[0].equals("Name")) {
                        fw.write("Name: " + newName + System.getProperty("line.separator"));
                    } else if (parts[0].equals("Street")) {
                        String newStreet2 = (newStreet.equals("")) ? parts[1] : newStreet;
                        fw.write("Street: " + newStreet2 + System.getProperty("line.separator"));
                    } else if (parts[0].equals("City")) {
                        String newCity2 = (newCity.equals("")) ? parts[1] : newCity;
                        fw.write("City: " + newCity2 + System.getProperty("line.separator"));
                    } else if (parts[0].equals("Email")) {
                        String newEmail2 = (newEmail.equals("")) ? parts[1] : newEmail;
                        fw.write("Email: " + newEmail2 + System.getProperty("line.separator"));
                    } else if (parts[0].equals("PhoneN")) {
                        String newPhone2 = (newPhone.equals("")) ? parts[1] : newPhone;
                        fw.write("PhoneN: " + newPhone2 + System.getProperty("line.separator"));
                    } else if (parts[0].equals("Balance")) {
                        fw.write(line + System.getProperty("line.separator"));
                    } else {
                        fw.write(line + System.getProperty("line.separator"));
                    }
                } else {
                    fw.write(line + System.getProperty("line.separator"));
                }

                if (parts[0].equals("BankNo") && parts[1].equals(Bnumber)) {
                    found = true;
                }

            }

            if (!found) {
                JOptionPane.showMessageDialog(this, "Bank number not found");
                return;
            }

            temp.delete();
            f.renameTo(temp);
            DefaultTableModel table = (DefaultTableModel) database.getModel();

            if (database.getSelectedRowCount() == 1) {
                String bnkNo = UpbankNo.getText();
                String owname = Upname.getText();
                String street = Upstreet.getText();
                String city = Upcity.getText();
                String email = Upemail.getText();
                String phone = Upphone.getText();

                table.setValueAt(bnkNo, database.getSelectedRow(), 0);
                table.setValueAt(owname, database.getSelectedRow(), 1);
                table.setValueAt(street, database.getSelectedRow(), 2);
                table.setValueAt(city, database.getSelectedRow(), 3);
                table.setValueAt(email, database.getSelectedRow(), 4);
                table.setValueAt(phone, database.getSelectedRow(), 5);
            }

            JOptionPane.showMessageDialog(this, "Data updated successfully");

        } catch (IOException ex) {
            Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

