    private void ShowdataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowdataActionPerformed
        //Show data from textfile
        File f = new File("C:\\Users\\jowie james\\Documents\\NetBeansProjects\\LoginApp\\UsersAccount.txt");

        if (!showDataClicked) {
            showDataClicked = true;

            try {
                BufferedReader br = new BufferedReader(new FileReader(f));
                DefaultTableModel table = (DefaultTableModel) database.getModel();

                String[] rowData = new String[6];

                int rowIndex = 0;
                String line;
                while ((line = br.readLine()) != null) {
                    if (!line.contains("Passcode:") && !line.startsWith("------") && !line.contains("Balance:")) {
                        String[] row = line.split(": ");
                        if (row.length >= 2) {
                            rowData[rowIndex] = row[1];
                            rowIndex++;
                        }
                        if (rowIndex == 6) {
                            
                            table.addRow(rowData);
                            
                            rowIndex = 0;
                            Arrays.fill(rowData, null);
                        }
                    }
                }
                
                if (rowIndex > 0 && rowIndex < 6) {
                    String[] lastRowData = new String[6];
                    System.arraycopy(rowData, 0, lastRowData, 0, rowIndex);
                    table.addRow(lastRowData);
                }
            } catch (Exception ex) {
                Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_ShowdataActionPerformed

