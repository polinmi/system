    private void Showdata1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Showdata1ActionPerformed
        //Show history data from txtfile.
        File f = new File(historyData);

        if (!showDataClicked1) {
            showDataClicked1 = true;

            try {

                BufferedReader br = new BufferedReader(new FileReader(f));
                DefaultTableModel table = (DefaultTableModel) TransacTable.getModel();

                String line;
                while ((line = br.readLine()) != null) {
                    String[] row = line.split(", ");
                    boolean historyExists = false;
                    for (int j = 0; j < table.getRowCount(); j++) {

                        if (table.getValueAt(j, 0).equals(row[0]) && table.getValueAt(j, 1).equals(row[1])
                                && table.getValueAt(j, 2).equals(row[2])) {

                            historyExists = true;
                            break;
                        }
                    }
                    if (!historyExists) {
                        table.addRow(row);
                    }

                }

            } catch (Exception ex) {
                Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_Showdata1ActionPerformed

