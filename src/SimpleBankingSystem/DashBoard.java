    private void cbEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEnterActionPerformed
        //getAccount

        File f = new File(customerAccountsFilePath);
        String bankNo = cBankNo.getText();
        String bnkN = "";
        String OwnerName = "";
        String bal = "";

        if (bankNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please input a Bank No.");
        } else {

            try {

                FileReader fr = new FileReader(f);
                BufferedReader br = new BufferedReader(fr);

                Object[] lines = br.lines().toArray();
                for (int i = 0; i < lines.length; i++) {
                    String[] row = lines[i].toString().split(": ");
                    if (row[0].equals("BankNo")) {
                        bnkN = row[1];
                        all_bankNo.add(bnkN);
                    } else if (row[0].equals("Name")) {
                        OwnerName = row[1];
                        all_usernames.add(OwnerName);
                    } else if (row[0].equals("Balance")) {
                        bal = row[1];
                        all_balance.add(bal);
                    }
                    if (!bnkN.equals("") && !OwnerName.equals("") && !bal.equals("")) {
                        bankNoANDnames.put(bnkN, OwnerName);
                    }
                    for (String n : bankNoANDnames.keySet()) {
                        if (n.equals(bankNo)) {
                            if (bankNoANDnames.get(n).equals(OwnerName)) {
                                cFName.setText(OwnerName);
                                cbBal.setText(bal);

                            }
                        }
                    }
                }
                System.out.println(all_bankNo);
                System.out.println(all_usernames);
                System.out.println(all_balance);

            } catch (FileNotFoundException ex) {
                Logger.getLogger(DashBoard.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_cbEnterActionPerformed

