    private void dDepositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dDepositButtonActionPerformed
        // Create a deposit function here
        String searchId = dAccNo.getText();
        String deposit = dDeposit.getText();
        String f = "customerAccountsFilePath";

        ArrayList<String> tempArray = new ArrayList<>();

        try {
            try {
                FileReader fr = new FileReader(f);
                Scanner reader = new Scanner(fr);
                String line;
                String[] lineArr;

                while ((line = reader.nextLine()) != null) {
                    lineArr = line.split(System.getProperty("line.separator") + ":");
                    if (lineArr[0].equals(searchId)) {
                        tempArray.add(
                                lineArr[0] + ","
                                + lineArr[1] + ","
                                + lineArr[2] + ","
                                + lineArr[3] + ","
                                + deposit);

                    } else {
                        tempArray.add(line);
                    }
                }
                fr.close();

            } catch (Exception e) {

            }
        } catch (Exception e) {

        }
        try {
            try (PrintWriter pw = new PrintWriter(f)) {

                for (String str : tempArray) {
                    pw.println(str);
                }
                pw.close();

            } catch (Exception e) {

            }
        } catch (Exception e) {

        }


    }//GEN-LAST:event_dDepositButtonActionPerformed

